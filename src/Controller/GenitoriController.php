          'certificati' => serialize($certificati), 'utente' => $this->getUser(),
          'ids' => explode(',', $info['assenza']['ids'])])
        ->getQuery()
        ->getResult();
      // memorizza dati
      $this->em->flush();
      // log azione
      if ($form->get('delete')->isClicked()) {
