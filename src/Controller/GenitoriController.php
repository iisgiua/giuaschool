          'ID' => $info['assenza']['ids'],
          'Giustificato' => $info['assenza']['giustificato'],
          'Motivazione' => $info['assenza']['motivazione'],
          'Dichiarazione' => $info['assenza']['dichiarazione'],
          'Certificati' => $info['assenza']['certificati']));
